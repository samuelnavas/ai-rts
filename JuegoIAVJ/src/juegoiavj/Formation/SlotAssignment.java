package juegoiavj.Formation;

import juegoiavj.kinematic.Kinematic;

/**
 *
 * @author Sandra
 */
public class SlotAssignment {
    private Kinematic character;
    private double SlotNumber;
    
    public SlotAssignment(Kinematic c){
        character = c;
    }

    public Kinematic getCharacter() {
        return character;
    }

    public void setCharacter(Kinematic character) {
        this.character = character;
    }

    public double getSlotNumber() {
        return SlotNumber;
    }
    void setSlotNumber(int SlotNumber) {
        this.SlotNumber=SlotNumber;
    }
    
}
