package juegoiavj.kinematic;

import java.util.List;

/**
 *
 * @author Samuel
 */
public class Behavior {
    private KinematicSteering behavior;
    private double weight;
    
    public Behavior(KinematicSteering behavior, double weight){
        this.behavior = behavior;
        this.weight = weight;
    }

    public KinematicSteering getBehavior() {
        return behavior;
    }

    public double getWeight() {
        return weight;
    }
    
    
}
